public class bitOperator {
    public static void main(String[] args) {
        System.out.println(5 & 6); // bitwise AND (&) -> 4
        System.out.println(5 | 6); // bitwise OR (|) -> 7
        System.out.println(5 ^ 6); // bitwise XOR (^) -> 3
        System.out.println( ~ 0 ); // bitwise not (~) -> 2
        System.out.println(5<< 2); // bitwise left Shift (<<) -> 20
        System.out.println(6 >>1); // bitwise Right Shift (>>) -> 3
    }
}